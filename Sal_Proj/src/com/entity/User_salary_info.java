package com.entity;

public class User_salary_info {
	private double BasicSalary;
	private double ClassFees;
	private double PostAllowance;
	public double getBasicSalary() {
		return BasicSalary;
	}
	public void setBasicSalary(double basicSalary) {
		BasicSalary = basicSalary;
	}
	public double getClassFees() {
		return ClassFees;
	}
	public void setClassFees(double classFees) {
		ClassFees = classFees;
	}
	public double getPostAllowance() {
		return PostAllowance;
	}
	public void setPostAllowance(double postAllowance) {
		PostAllowance = postAllowance;
	}
	
}
